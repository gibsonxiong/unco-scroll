              ※※※<br>
              当袋底洞的比尔博·巴金斯先生宣布不久后会为自己一百一十一岁大寿举行盛大宴会时，哈比屯的居民都兴奋的议论纷纷。<br>
              比尔博不但非常富有，更是个特立独行的奇人。自从他神秘的失踪和奇迹似的归来之后，六十年以来，他在夏尔这一带一直是人们街头巷尾的议论话题。他从冒险途中所带回的庞大财富已经成了当地的传奇，不管这老家伙怎么说，一般人都相信袋底洞内的隧道装满各种各样的金银珠宝。即使这样的传奇不够让他出名，他老当益壮的外表也足以让人啧啧称奇。时间的流逝似乎在比尔博身上没有留下多少痕迹。他九十岁的时候与五十岁时并无二致。当他九十九岁时，附近的人开始称他“养生有术”，但恐怕“长生不老”是比较精确的说法。有许多人一想到这件事情就觉得老天未免太不公平了，怎么能够让人坐拥（传说中的）金山又同时拥有长生不老的能力呢。<br>
              “这一定是有代价的，”他们说。“这是违逆天理的，一定会惹麻烦的！”<br>
              不过，到目前为止也没有什么麻烦；由于巴金斯先生十分慷慨，人们也就愿意原谅他的特立独行和得天独厚的好运。他依旧时常拜访亲戚（当然，素来不合的塞克维尔巴金斯一家是个例外），在地位较低和贫穷的家族中，他也拥有许多的崇拜者。不过，他一直没有什么亲近的朋友，直到他的年轻表亲年纪稍长之后才有了转变。<br>
              这些表亲之中最年长的是佛罗多·巴金斯，同时也是比尔博最宠爱的对象。当比尔博九十九岁的时候，他将佛罗多收为养子，接他到袋底洞来住。这终于打破了塞克维尔一家人一直觊觎继承袋底洞的希望。比尔博和佛罗多刚好都是同一天生的，九月二十二日。“佛罗多啊，我说你最好过来跟我一起住吧，”比尔博有天这么说；“这样我们就可以一起舒舒服服的过生日了。”当时佛罗多还只是个少年。哈比人一向把成年的三十三岁和童年之间的二十多年间称做少年时期。<br>
              又过了十二年。每年这家人都会联合举办盛大的生日宴会，不过，现在大家都知道今年秋天的计划是非比寻常的。比尔博今年将满一百一十一岁，数字本身就相当特殊；即使对哈比人来说，这也已经是十分长寿的年纪了。（老图克大人也不过只活了一百三十岁）。而佛罗多今年则是满三十三岁，这是很重要的一个数字，因为今年他即将成年。<br>
              哈比屯和临水区一带的居民早就开始议论纷纷，有关这即将来临的大活动也传遍了整个夏尔。比尔博·巴金斯先生的冒险经历和独特的行事作风又再度成为街头巷尾的话题，老一辈的人突然间发现自己在这股怀旧风潮的推波助澜下，成了十分受欢迎的人物。<br>
              被称作“老家伙”的哈姆·詹吉可说是个中的翘楚。他经常在临水路旁的“常春树丛”小旅店高谈阔论。他可不是毫无依据的吹牛，老家伙已经照顾袋底洞的花园有四十年之久。由于他年事已高，动作有些迟缓了，因此大多数的工作都是由他最小的儿子山姆·詹吉来接手。父子两人都与比尔博和佛罗多十分友好。他们就居住在袋底洞的山下小丘上，地址是袋边路三号。<br>
              “我老早就说，比尔博先生是个相当好的哈比人，”老家伙宣称。这是千真万确的，因为比尔博对他非常有礼貌，平常都称呼他为哈姆法斯特先生，并且经常的在蔬菜种植的问题上请教他，特别是在根茎类植物的种植上更是执礼甚恭。老家伙在马铃薯这类植物方面可是第一把交椅。（连他自己也不吝承认）<br>
              “但和他住在一起的佛罗多又怎么样？”临水区的老诺克问道。“他也叫作巴金斯，但他也有一半烈酒鹿家的血统。我也搞不清楚为什么会有哈比屯的巴金斯家人想要去找雄鹿地的怪家伙结婚。”<br>
              “也不能怪他们不合常理，”老家伙的隔壁邻居图伏特老爹说，“他们住在烈酒河的另一边，又靠近老林那边。那里可是个受诅咒的不祥之地。”<br>
              “你说的对，老爹！”老家伙说。“虽然雄鹿地的烈酒鹿那家子人不是住在老林里面，但他们的行事作风真的很奇怪。他们会在那条大河上搞艘船跑来跑去，这可不是正当人家会做的事情。难怪那里老是会有麻烦事。不管怎么样，佛罗多先生都是个好青年。他和比尔博先生很像，连想法都差不了多少。毕竟他父亲那边还是有巴金斯家的血统。德罗哥·巴金斯可是个好人，在他淹死之前可是个洁身自爱的家伙哪！”<br>
              “淹死？”听众中有人反问。他们当然听过这类恐怖的谣言，不过哈比人就是喜欢这种家族历史的故事；他们这次又想再听一次。<br>
              “嗯，他们是这样说的，”老家伙道。“我想一下喔，德罗哥先生娶了可怜的普丽谬拉·烈酒鹿小姐。她是比尔博先生母系的表妹（她妈妈是老图克最小的女儿）；德罗哥则是他的旁系亲戚。所以，佛罗多就是比尔博的表妹的儿子，这关系可深远着哪！德罗哥先生结婚之后就经常和去烈酒厅的岳父家斯混。（这家伙嘴可馋着呢，他岳父葛巴达克又爱吃好菜，两人就这么一拍即合）当时他去烈酒河上泛舟，他和他的妻子就这么翻船淹死了。可怜的佛罗多那时还只是个小孩啊！”<br>
              “我听说他们是吃完饭之后准备在月光下泛舟，”老诺克说，“德罗哥吃得太多，把船给压沉了。”<br>
              “我听说是她把他推下去，而德罗哥又把老婆给拉下去，”哈比屯的磨坊主人山迪曼接口道。<br>
              “我说山迪曼哪，你最好不要把这些谣言照单全收，”老家伙不太喜欢眼前的磨坊主人。“老是提一些推推拉拉的事情没意思嘛！船这种东西很危险，就算你坐好不动，不想惹麻烦，还是有可能倒楣的。不管啦，反正佛罗多最后就是成了孤儿，被丢在那群雄鹿地的怪人之中，在烈酒厅被养大。那里就像个大杂院一样。葛巴达克大人在那边起码有几百个亲戚。比尔博先生把这位小朋友带回来教养，真是做了件好事啊！”<br>
              “不过，我也明白这对于那些巴金斯家的塞克维尔一系人来说是个重大打击。当年在比尔博先生失踪，大家都以为他去世之后，他们满心以为终于可以继承袋底洞。他却又神秘出现，把他们赶了出来。而且老天保佑，比尔博先生越活越硬朗，一点都看不出来老态！突然，他又找了个继承人，备齐了一切的文件。我看这回塞克维尔他们是想都不要想踏进袋底洞一步了。我自己也希望那里不要被他们糟蹋了。”<br>
              “我听说那里面藏了很多钱耶，”一个从西区米丘窟来做生意的陌生人说。“这座山里面的隧道全都装满了许多箱子，里面都是黄金、白银和珠宝。”<br>
              “你听说的比我知道的还要多，”老家伙回答。“我不知道什么珠宝。比尔博先生对钱财很大方，手头也很阔绰，但我没听说什么挖隧道的事情。大概六十年前，我小时候亲眼见到比尔博先生回来的样子。那时我才刚当上老何曼的学徒（他是我爹的表亲），他派我去袋底洞维持秩序，避免在拍卖的时候让闲杂人等把花园给踩乱了。正当大家急着拍卖比尔博先生的老家和财产时，他突然牵着小马走上这座山，马背上还有好几个大袋子和箱子。我想那里面一定都是从外面世界带回来的财宝；有人说外面有很多金山。但是，我看到的东西也不够把隧道塞满。我儿子山姆大概会知道的更清楚。他常常进出袋底洞。这孩子最喜欢听故事，所有比尔博先生的故事他都背的滚瓜烂熟。比尔博先生甚至还教他识字，各位别露出那种表情，他可是一片好心。但愿不会有什么麻烦才好。”<br>
              “‘老是想搞一些有关那些精灵与龙的故事！’我这样说。‘莴苣和马铃薯对你我来说才是比较适合的念头。别老是好高骛远，想要和比我们高贵几百倍的人物打交道，不然你会惹上大麻烦的，’我一向都这样告诫他。其他人最好也听我的劝告，”他看了那陌生人和磨坊主人一眼。<br>
              不过，老家伙的警告没办法说服他的听众。比尔博传说中的财富在年轻的哈比人心中可说是根深蒂固的传奇，无法动摇了。<br>
              “啊，不过他也可能后来又赚到更多的钱，”磨坊主人的论调和大多数人一样，“他常常离家去旅行。你们看看那些拜访他的外地人：晚上出现的矮人、那个老法师甘道夫等等。老家伙，你爱怎么说都没关系，但袋底洞真的是个诡异的地方，里面住的人更奇怪。”<br>
              “我说山迪曼，你爱说什么也都没关系，反正大家也清楚你知道的其实有限，就跟你不会划船一样。”老家伙这次比平常更讨厌这个磨坊主人了。“如果那样就叫诡异，那我们这一带还真的需要多一些这种诡异。其它地方有些一毛不拔的家伙就算住在金山里，也不愿意请朋友喝啤酒。袋底洞可是以慷慨待人出了名的。我们家的山姆说这次每个人都会受邀参加宴会，听好喔，每个人都还会有礼物！就在这个月！”<br>
              这个月就是九月，天气依旧十分的怡人。一两天之后，到处就开始流传一个谣言（多半是情报灵通的山姆放出来的消息）：据说这次宴会有烟火！而且，这次的烟火将会是百年来夏尔第一次盛大的烟火表演；自从上次老图克过世之后就没人见过烟火表演了。<br>
              ※※※<br>
              日子一天天过去，大日子也越来越接近。某天傍晚，一辆装满怪异包裹的怪异马车开进哈比屯，在袋底洞前停了下来。吃惊的哈比人纷纷从窗内往外窥探。驾车的是形迹怪异的外地人，唱着没人听过的歌谣：车夫是有着长胡子带着兜帽的矮人。几名矮人甚至还在袋底洞留了下来。到了九月第二周的时候，另一辆马车在光天化日之下越过烈酒桥，沿着临水区开了过来。驾车的只有一名老人。他戴着一顶高高尖尖的蓝色帽子，穿着长长的灰袍以及一条银色的围巾。他的胡子又白又长，眉毛也长到伸出了帽缘。一大群小孩跟着马车后面跑，穿过整个哈比屯，沿路跟着上了小山。他们猜的果然没错，车内装的是烟火。老人在比尔博的门前开始卸货，里面有各种各样的烟火，每个都标明着一个大的红色“甘”和精灵文字。<br>
              这就是甘道夫的徽记，而那老人就是巫师甘道夫。他在夏尔的名气主要是关于他操纵火焰、烟雾和光线的技巧。他真正的工作比这还要复杂、危险的多，但单纯的夏尔居民对此一无所知。对他们来说，这巫师只是宴会的另一大卖点。因此小孩们才会这么兴奋。“这缩写是壮丽的意思！”孩子们大喊着，老人报以慈祥的微笑。虽然他偶尔才会来拜访此地，每次也不会停留很长的时间，但哈比人每个都知道他的长相。不过，包括这些小孩和目前最老的哈比人都一样没看过他的烟火表演；这伟大的表演现在只存在于史书的记载中了。<br>
              老人在比尔博和几名矮人的帮助下完成卸货之后，比尔博给了这群小孩一些零钱。不过，孩子们失望的发现，今天比尔博叔叔没有饼干或零嘴可以给他们。<br>
              “快回家吧！”甘道夫说。“时候到了你们会吃都吃不完的。”然后他就和比尔博一起走进屋内，关上大门。年轻的哈比人们呆呆的看了大门半晌，最后才拖着不情愿的脚步离开，满心觉得宴会彷佛永远都不会到。在袋底洞里，比尔博和甘道夫坐在俯瞰花园的窗户下。傍晚的天色还很明亮，天气也很温和。红色和金色的花朵生长的十分茂盛，龙嘴花和向日葵都露出欣欣向荣的态势，金莲花则是生气勃勃的攀上窗子，看着屋内的情景。<br>
              “你的花园看起来真漂亮！”甘道夫说。<br>
              “没错，”比尔博回答。“我很喜欢这个花园，夏尔对我来说也一样的亲切。不过，我想也该是放个假的时候了。”<br>
              “你是说要继续你原先的计划吗？”<br>
              “是的。我几个月前就下定了决心，现在也不会临时变卦。”<br>
              “很好。那我们就不必多说了。不要心软，照着原订的计划进行。记住，是原订的计划，我希望这会为你，也为我们大家带来好结果。”<br>
              “我也这么希望。反正我准备这周四好好的享受一下，让大家看看我的小玩笑。”<br>
              “不知道最后谁会笑啊？”甘道夫摇着头说。<br>
              “到时就知道了，”比尔博回答。<br>
